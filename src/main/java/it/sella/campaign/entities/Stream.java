package it.sella.campaign.entities;

import java.util.Set;

public class Stream {

	private String streamName;
	private Set<String> streamCollection;
	public String getStreamName() {
		return streamName;
	}
	public void setStreamName(String streamName) {
		this.streamName = streamName;
	}
	public Set<String> getStreamCollection() {
		return streamCollection;
	}
	public void setStreamCollection(Set<String> streamCollection) {
		this.streamCollection = streamCollection;
	}
	
	
	
}
